from validation import PurgedWalkForwardCV

def test_meta_stacker_no_leakage():
    """
    Asserts that the training features for the Meta Decision Engine (stacker)
    are constructed strictly out-of-fold, ensuring that the base model
    predictions for any given index were generated by a base model instance
    which was NOT trained on that index.
    """
    n_samples = 100
    dates = pd.date_range("2026-07-05", periods=n_samples, freq="min", tz="UTC")
    df = pd.DataFrame({"timestamp": dates, "close": [100.0] * n_samples})
    
    # Mock labels df with no overlaps for simplicity
    labels_df = pd.DataFrame({
        "label": [1.0] * n_samples,
        "exit_time": dates
    })
    
    cv = PurgedWalkForwardCV(n_folds=3, embargo_bars=1)
    
    # Track which training indices each base model is trained on
    # and match them against the indices of predictions we use for the stacker
    stacker_inputs = []
    stacker_targets = []
    
    # A dictionary mapping index -> list of indices used to train the model that predicted it
    prediction_provenance = {}
    
    fold_idx = 0
    for train_idx, test_idx in cv.split(df, labels_df):
        # In actual code: base_model is fit on train_idx
        # And base_model predicts on test_idx
        
        # Log provenance: for each test index, the model that generated its prediction
        # was trained on train_idx.
        for t_idx in test_idx:
            prediction_provenance[t_idx] = list(train_idx)
            
        fold_idx += 1
        
    # Assertions to verify no in-sample leakage:
    # For every sample index that enters the stacker training dataset,
    # the base model predictions MUST have been generated by a model
    # that did NOT train on that sample index.
    
    assert len(prediction_provenance) > 0, "No out-of-sample predictions were generated."
    
    for oos_index, train_indices_used in prediction_provenance.items():
        # The key test: the target index itself must NOT be in the train indices used by the model
        assert oos_index not in train_indices_used, (
            f"LEAKAGE DETECTED: Prediction for index {oos_index} was generated "
            f"by a base model trained on index {oos_index} (In-Sample Leakage)."
        )
        
    # Check that the total unique OOS indices cover the expected cross-validation range
    # Expanding window CV leaves the first fold out of test (used only for initial training).
    # Total OOS indices should equal the test indices of folds 1, 2, 3.
    # Segment size = 100 / 4 = 25.
    # Fold 1 test: 25-49, Fold 2 test: 50-74, Fold 3 test: 75-99.
    expected_oos_indices = list(range(25, 100))
    for idx in expected_oos_indices:
        assert idx in prediction_provenance, f"Index {idx} missing from OOS prediction set."
        
    print("SUCCESS: Zero in-sample leakage detected. Stacker training is strictly out-of-fold!")
